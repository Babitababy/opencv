import cv2
import numpy as np

img1=np.zeros((250,250,3),np.uint8)
img1=cv2.rectangle(img1,(100,100),(200,200),(255,255,255),-1)
#img2=cv2.imread('imagegray.png')
img2=np.zeros((250,250,3),np.uint8)
img2=cv2.rectangle(img2,(200,0),(300,200),(255,255,255),-1)
bitAnd=cv2.bitwise_and(img2,img1)
bitor=cv2.bitwise_or(img2,img1)
bitnot=cv2.bitwise_not(img2,img1)
bitxor=cv2.bitwise_xor(img2,img1)
cv2.imshow("Img1",img1)
cv2.imshow("Img2",img2)
cv2.imshow("Bitwise And",bitAnd)
cv2.imshow("OR",bitor)
cv2.imshow("Not",bitnot)
cv2.imshow("xor",bitxor)
cv2.waitKey(0)
cv2.destroyAllWindows()
